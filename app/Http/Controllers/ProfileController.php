    /**
     * Display the specified resource.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function show(User $user)
    {
        $user->library;
        $user->recentlyWatched;
        $profile = $user;
        foreach($profile->library as $library) {
            $library->series;
            $library->status = Library::status($library->status);
        }

        foreach($profile->recentlyWatched as $recent) {
            $recent->series;
            $recent->time = Episode::timeElapsedString($recent->created_at);
        }

        return View::component('Profile', [
            'profile' => $profile,
        ]);
    }

